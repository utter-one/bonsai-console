#!/usr/bin/env node
/**
 * Generate TypeScript types from websocket-contracts.json
 * 
 * This script reads the JSON Schema definitions and generates
 * TypeScript type definitions for all WebSocket message types.
 */

import { compile } from 'json-schema-to-typescript'
import { readFileSync, writeFileSync } from 'fs'
import { join, dirname } from 'path'
import { fileURLToPath } from 'url'

const __filename = fileURLToPath(import.meta.url)
const __dirname = dirname(__filename)

const schemaPath = join(__dirname, '../src/api/websocket/websocket-contracts.json')
const outputPath = join(__dirname, '../src/api/websocket/websocket-contracts.ts')

async function generateTypes() {
  console.log('📄 Reading JSON Schema...')
  const schema = JSON.parse(readFileSync(schemaPath, 'utf-8'))

  const definitions = schema.definitions || {}
  const definitionNames = Object.keys(definitions)

  console.log(`✨ Found ${definitionNames.length} type definitions`)

  // Auto-discover ASR settings types (any type ending in "AsrSettings")
  const asrSettingsTypes = definitionNames.filter(name => name.endsWith('AsrSettings'))
  console.log(`🔍 Auto-discovered ${asrSettingsTypes.length} ASR settings types:`, asrSettingsTypes)

  // Header comment
  let output = `/**
 * WebSocket Message Contracts
 * TypeScript type definitions for all WebSocket message types in the Bonsai Backend API
 * 
 * @generated This file is auto-generated from websocket-contracts.json
 * Do not edit this file directly. Run 'npm run generate:ws-types' to regenerate.
 */

`

  // Shared types that are referenced by other types
  const sharedTypes = [
    'ImageParameterValue',
    'AudioParameterValue',
    'ParameterValue',
    'Effect',
    'EndConversationEffect',
    'AbortConversationEffect',
    'GoToStageEffect',
    'RunScriptEffect',
    'ModifyUserInputEffect',
    'ModifyVariablesEffect',
    'VariableOperation',
    'ModifyUserProfileEffect',
    'UserProfileOperation',
    'CallToolEffect',
    'CallWebhookEffect',
    'GenerateResponseEffect'
  ]

  // Group definitions by category
  const categories = {
    shared: [
      'ImageParameterValue', 'AudioParameterValue', 'ParameterValue',
      'Effect', 'EndConversationEffect', 'AbortConversationEffect',
      'GoToStageEffect', 'RunScriptEffect', 'ModifyUserInputEffect',
      'ModifyVariablesEffect', 'VariableOperation', 'ModifyUserProfileEffect',
      'UserProfileOperation', 'CallToolEffect', 'CallWebhookEffect',
      'GenerateResponseEffect'
    ],
    auth: ['auth-request', 'auth-response', 'project-settings', ...asrSettingsTypes],
    session: [
      'start-conversation-request', 'start-conversation-response',
      'resume-conversation-request', 'resume-conversation-response',
      'end-conversation-request', 'end-conversation-response',
      'conversation-event'
    ],
    userInput: [
      'start-user-voice-input-request', 'start-user-voice-input-response',
      'send-user-voice-chunk-request', 'send-user-voice-chunk-response',
      'end-user-voice-input-request', 'end-user-voice-input-response',
      'send-user-text-input-request', 'send-user-text-input-response',
      'user-transcribed-chunk'
    ],
    aiResponse: [
      'start-ai-generation-output', 'send-ai-voice-chunk', 'end-ai-generation-output',
      'ai-transcribed-chunk', 'send-ai-audio-output', 'send-ai-image-output'
    ],
    command: [
      'go-to-stage-request', 'go-to-stage-response',
      'set-var-request', 'set-var-response',
      'get-var-request', 'get-var-response',
      'get-all-vars-request', 'get-all-vars-response',
      'run-action-request', 'run-action-response',
      'call-tool-request', 'call-tool-response'
    ]
  }

  // Extract AudioFormat enum from schema
  const audioFormatEnums = definitions['send-ai-voice-chunk']?.properties?.audioFormat?.enum
  if (audioFormatEnums && Array.isArray(audioFormatEnums)) {
    const audioFormatUnion = audioFormatEnums.map(f => `'${f}'`).join(' | ')
    output += `// ============================================================================
// Audio Types (Shared)
// ============================================================================

export type AudioFormat = ${audioFormatUnion}`
    console.log(`  → Generated AudioFormat with ${audioFormatEnums.length} formats`)
  } else {
    console.warn('⚠️  Could not find audioFormat enum in schema')
  }

  // Process other categories
  const categoryHeaders = {
    shared: 'Shared Types (Parameters)',
    auth: 'Authentication and Project Settings',
    session: 'Session and Conversation Lifecycle',
    userInput: 'User Input (Voice and Text)',
    aiResponse: 'AI Response (Voice Output)',
    command: 'Commands (Stage Navigation, Variables, Actions)'
  }

  // Track generated types to avoid duplicates
  const generatedTypes = new Set()

  // Check for uncategorized types
  const allCategorizedTypes = new Set()
  Object.values(categories).forEach(categoryTypes => {
    categoryTypes.forEach(type => allCategorizedTypes.add(type))
  })

  const uncategorizedTypes = definitionNames.filter(name => !allCategorizedTypes.has(name))
  if (uncategorizedTypes.length > 0) {
    console.warn(`\n⚠️  Found ${uncategorizedTypes.length} uncategorized types:`)
    uncategorizedTypes.forEach(type => console.warn(`   - ${type}`))
    console.warn('   These types exist in the schema but are not in any category and will not be generated.\n')
  }

  for (const [category, names] of Object.entries(categories)) {
    output += `\n// ============================================================================
// ${categoryHeaders[category]}
// ============================================================================

`

    for (const name of names) {
      if (!definitions[name]) {
        console.warn(`⚠️  Definition '${name}' not found in schema`)
        continue
      }

      // Convert kebab-case to PascalCase for type name
      const typeName = name
        .split('-')
        .map(word => word.charAt(0).toUpperCase() + word.slice(1))
        .join('')

      // Skip if already generated
      if (generatedTypes.has(typeName)) {
        console.log(`  ⏭️  Skipping ${typeName} (already generated)`)
        continue
      }

      try {
        console.log(`  → Generating ${typeName}...`)

        const typeDefinition = definitions[name]

        // For shared types, also need definitions for $ref within shared types
        // But we need to prevent duplicate generation
        const schemaToCompile = {
          ...typeDefinition,
          definitions: schema.definitions,
        }

        const compiled = await compile(
          schemaToCompile,
          typeName,
          {
            bannerComment: '',
            style: {
              singleQuote: true,
            },
            additionalProperties: false,
            declareExternallyReferenced: false,
          }
        )

        // Clean up the output (remove extra newlines)
        let cleaned = compiled
          .replace(/\n{3,}/g, '\n\n')
          .trim()

        // For non-shared types, remove duplicate definitions of shared types that were already generated
        if (category !== 'shared') {
          // Remove duplicate ImageParameterValue, AudioParameterValue, ParameterValue definitions
          for (const sharedType of sharedTypes) {
            const typeRegex = new RegExp(
              `\\/\\*\\*[\\s\\S]*?\\*\\/\\s*export (interface|type) ${sharedType}[\\s\\S]*?(?=\nexport |$)`,
              'g'
            )
            cleaned = cleaned.replace(typeRegex, '')
          }
          // Clean up extra blank lines after removal
          cleaned = cleaned.replace(/\n{3,}/g, '\n\n').trim()
        }

        // Fix metadata object index signatures that conflict with specific properties
        // Remove lines like:  [k: string]: { [k: string]: unknown; };
        // or  [k: string]: unknown;
        // that appear within metadata objects
        cleaned = cleaned.replace(/^\s*\[k: string\]:\s*(\{[^}]+\}|unknown);?\s*$/gm, '')

        // Clean up empty metadata objects or trailing commas
        cleaned = cleaned.replace(/metadata\?: \{\s*\};?/g, 'metadata?: Record<string, unknown>;')
        cleaned = cleaned.replace(/,(\s*\})/g, '$1') // Remove trailing commas before closing braces

        cleaned = cleaned.trim()
        if (cleaned) {
          output += cleaned + '\n\n'
          generatedTypes.add(typeName)
        }
      } catch (error) {
        console.error(`❌ Error generating type for '${name}':`, error.message)
      }
    }
  }

  // Write the output file
  console.log(`\n💾 Writing to ${outputPath}...`)
  writeFileSync(outputPath, output, 'utf-8')

  console.log('✅ TypeScript types generated successfully!')
  console.log(`📦 Generated ${definitionNames.length} type definitions`)
}

generateTypes().catch(error => {
  console.error('❌ Error:', error)
  process.exit(1)
})
