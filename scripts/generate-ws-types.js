#!/usr/bin/env node
/**
 * Generate TypeScript types from websocket-contracts.json
 * 
 * This script reads the JSON Schema definitions and generates
 * TypeScript type definitions for all WebSocket message types.
 */

import { compile } from 'json-schema-to-typescript'
import { readFileSync, writeFileSync } from 'fs'
import { join, dirname } from 'path'
import { fileURLToPath } from 'url'

const __filename = fileURLToPath(import.meta.url)
const __dirname = dirname(__filename)

const schemaPath = join(__dirname, '../src/api/websocket/websocket-contracts.json')
const outputPath = join(__dirname, '../src/api/websocket/websocket-contracts.ts')

async function generateTypes() {
  console.log('📄 Reading JSON Schema...')
  const schema = JSON.parse(readFileSync(schemaPath, 'utf-8'))
  
  const definitions = schema.definitions || {}
  const definitionNames = Object.keys(definitions)
  
  console.log(`✨ Found ${definitionNames.length} type definitions`)
  
  // Header comment
  let output = `/**
 * WebSocket Message Contracts
 * TypeScript type definitions for all WebSocket message types in the Nexus Backend API
 * 
 * @generated This file is auto-generated from websocket-contracts.json
 * Do not edit this file directly. Run 'npm run generate:ws-types' to regenerate.
 */

`

  // Group definitions by category
  const categories = {
    audio: ['AudioFormat'],
    auth: ['auth-request', 'auth-response', 'project-settings'],
    session: [
      'start-conversation-request', 'start-conversation-response',
      'resume-conversation-request', 'resume-conversation-response',
      'end-conversation-request', 'end-conversation-response'
    ],
    userInput: [
      'start-user-voice-input-request', 'start-user-voice-input-response',
      'send-user-voice-chunk-request', 'send-user-voice-chunk-response',
      'end-user-voice-input-request', 'end-user-voice-input-response',
      'send-user-text-input-request', 'send-user-text-input-response',
      'user-transcribed-chunk'
    ],
    aiResponse: [
      'start-ai-generation-output', 'send-ai-voice-chunk', 'end-ai-generation-output',
      'ai-transcribed-chunk'
    ],
    command: [
      'go-to-stage-request', 'go-to-stage-response',
      'set-var-request', 'set-var-response',
      'get-var-request', 'get-var-response',
      'get-all-vars-request', 'get-all-vars-response',
      'run-action-request', 'run-action-response'
    ]
  }

  // Process AudioFormat specially (it's an enum)
  output += `// ============================================================================
// Audio Types (Shared)
// ============================================================================

export type AudioFormat = 'pcm_16000' | 'pcm_22050' | 'pcm_44100'

`

  // Process other categories
  const categoryHeaders = {
    auth: 'Authentication and Project Settings',
    session: 'Session and Conversation Lifecycle',
    userInput: 'User Input (Voice and Text)',
    aiResponse: 'AI Response (Voice Output)',
    command: 'Commands (Stage Navigation, Variables, Actions)'
  }

  for (const [category, names] of Object.entries(categories)) {
    if (category === 'audio') continue // Already handled
    
    output += `// ============================================================================
// ${categoryHeaders[category]}
// ============================================================================

`
    
    for (const name of names) {
      if (!definitions[name]) {
        console.warn(`⚠️  Definition '${name}' not found in schema`)
        continue
      }
      
      try {
        // Convert kebab-case to PascalCase for type name
        const typeName = name
          .split('-')
          .map(word => word.charAt(0).toUpperCase() + word.slice(1))
          .join('')
        
        console.log(`  → Generating ${typeName}...`)
        
        const typeDefinition = definitions[name]
        
        // Compile the type
        const compiled = await compile(typeDefinition, typeName, {
          bannerComment: '',
          style: {
            singleQuote: true,
          },
          additionalProperties: false,
        })
        
        // Clean up the output (remove extra newlines)
        const cleaned = compiled
          .replace(/\n{3,}/g, '\n\n')
          .trim()
        
        output += cleaned + '\n\n'
      } catch (error) {
        console.error(`❌ Error generating type for '${name}':`, error.message)
      }
    }
  }

  // Write the output file
  console.log(`\n💾 Writing to ${outputPath}...`)
  writeFileSync(outputPath, output, 'utf-8')
  
  console.log('✅ TypeScript types generated successfully!')
  console.log(`📦 Generated ${definitionNames.length} type definitions`)
}

generateTypes().catch(error => {
  console.error('❌ Error:', error)
  process.exit(1)
})
